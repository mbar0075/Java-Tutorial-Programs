
class Students
{
    private String name;
    private String surname;
    private String phone;

    public Students(String nameIn, String surnameIn, String phoneIn)
    {
        name = nameIn;
        surname = surnameIn;
        phone = phoneIn;
    }

    public String getName()
    {
        return name;
    }

    public String getSurname()
    {
        return surname;
    }

    public String getPhone()
    {
        return phone;
    }
}


