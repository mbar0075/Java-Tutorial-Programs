

public class Circle
{
    public static final double PI = 3.142;  // class variable
    
    public  double getArea(double radius)  // getter 
    {
        return (PI  * radius * radius);
       }
       
       
       public  double getCircumference(double radius)// getter
       {
           return (PI * 2* radius);
        }
        
    }
	    